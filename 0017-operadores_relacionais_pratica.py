numero1 = int(input("Digite um número:"))
numero2 = int(input("Digite um segundo número:"))

if(numero1==numero2):
    print("O número %i é igual ao número %i" %(numero1, numero2))
if(numero1!=numero2):
    print("O número %i é diferente do número %i" %(numero1, numero2))
if(numero1<numero2):
    print("O número %i é menor que o número %i" %(numero1, numero2))
if(numero1>numero2):
    print("O número %i é maior que o número %i" %(numero1, numero2))
if(numero1>=numero2):
    print("O número %i é maior ou igual que o número %i" %(numero1, numero2))
if(numero1<=numero2):
    print("O número %i é menor ou igual que o número %i" %(numero1, numero2))


